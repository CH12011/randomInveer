def generate_weighted_random():
    return random.choices(
        population=[1, 2, 3],
        weights=[0.5, 0.3, 0.2],
        k=1
    )[0]

@app.route('/')
def index():
    return render_template('index.html', ascii_art=ASCII_ART)

@app.route('/api/random', methods=['GET'])
def api_random():
    random_number = generate_weighted_random()
    return jsonify({
        'number': random_number,
        'ascii_art': ASCII_ART
    })

@app.route('/api/v', methods=['GET'])
def api_number_only():
    random_number = generate_weighted_random()
    return str(random_number)

